class Alumno:
    def __init__(self, apellido, nombre, nro_documento, tipo_documento, fecha_nacimiento, sexo, nro_legajo, fecha_ingreso):
        self.apellido = apellido
        self.nombre = nombre
        self.nro_documento = nro_documento
        self.tipo_documento = tipo_documento
        self.fecha_nacimiento = fecha_nacimiento
        self.sexo = sexo
        self.nro_legajo = nro_legajo
        self.fecha_ingreso = fecha_ingreso